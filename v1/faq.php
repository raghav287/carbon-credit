                                <div class="faq-accordion-answer">
                                    <p>
                                        Scope 2 includes indirect emissions associated with
                                        purchased electricity, heating, cooling or steam
                                        consumed by the organisation.
                                    </p>
                                </div>
                            </details>

                            <details class="faq-accordion-item">
                                <summary>
                                    What are Scope 3 emissions?

                                    <span class="faq-accordion-toggle" aria-hidden="true"></span>
                                </summary>

                                <div class="faq-accordion-answer">
                                    <p>
                                        Scope 3 includes other indirect emissions across the
                                        upstream and downstream value chain. Examples include
                                        purchased goods, transportation, business travel,
                                        employee commuting and product use.
                                    </p>
                                </div>
                            </details>

                            <details class="faq-accordion-item">
