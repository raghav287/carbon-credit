                                    <p>
                                        A carbon footprint report summarises greenhouse-gas
                                        emissions for a defined organisation, activity,
                                        product or reporting period. The exact boundary and
                                        methodology should be established before measurement.
                                    </p>
                                </div>
                            </details>

                            <details class="faq-accordion-item">
